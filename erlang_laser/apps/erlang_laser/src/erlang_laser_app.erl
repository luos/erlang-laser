-module(erlang_laser_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {ok, Pid} = erlang_laser_sup:start_link(),
    {ok, Pid}.

%%--------------------------------------------------------------------
stop(_State) ->
    lager:info("Stopping...", [_State]),
    ok.

